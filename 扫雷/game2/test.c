#define _CRT_SECURE_NO_WARNINGS 1


#include "game.h"

void menu()
{
	printf("*************************\n");
	printf("********  1.play  *******\n");
	printf("********  0.exit  *******\n");
	printf("*************************\n");
}

void game()
{
	//雷的信息的存储
	//1.布置好的雷的信息存储
	char mine[ROWS][COLS] = { 0 };//11*11的数组
	
	//2.排查出的雷的信息存储
	char show[ROWS][COLS] = { 0 };
	//将数组信息初始化
	InitBoard(mine, ROWS, COLS, '0');
	InitBoard(show, ROWS, COLS, '*');
	//打印棋盘
	//DisplayBoard(mine, ROW, COL);
	DisplayBoard(show, ROW, COL);
	//布置雷
	SetMine(mine,ROW,COL);	
	//扫雷
	FindMine(mine,show,ROW,COL);//将mine中的雷的信息找到，放到show函数里
}

void test()
{
	srand((unsigned int)time(NULL));
	int input = 0;
	do
	{
		menu();
		printf("请选择:>\n");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误，请重新选择:>");
			break;
		}
	} while (input);
}

int main()
{
	test();
	return 0;
}